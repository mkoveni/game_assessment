package dev.rivalani.unitconverter.http;

public class ConversionResponse {
    private double amout;
    private String unit;

    public double getAmout() {
        return amout;
    }

    public void setAmout(double amout) {
        this.amout = amout;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

}
